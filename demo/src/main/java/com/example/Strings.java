package com.example;

public class Strings {
    public static void main(String[] args) {
        String greeting = "Hello";
        String location = "Seoul";
        String result = greeting + " " + location + "!";
        System.out.println(result);

        String s1 = "a";
        String s2 = s1 + 4;
        System.out.println(s2); // a4
    }
}
